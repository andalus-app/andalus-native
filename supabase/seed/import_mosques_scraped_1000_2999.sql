INSERT INTO mosques
  (name, address, postal_code, city, country, latitude, longitude,
   wheelchair_accessible, opening_hours, status, address_source, address_verified, approved_at)
SELECT
  i.name, i.address, i.postal_code, i.city, 'Sweden',
  i.latitude::double precision, i.longitude::double precision,
  i.wheelchair_accessible::boolean,
  '{"alla":"Dygnet runt"}'::jsonb,
  'approved', 'import', FALSE, now()
FROM incoming i
WHERE NOT EXISTS (
  SELECT 1 FROM mosques m
  WHERE 6371000 * 2 * asin(sqrt(
        power(sin(radians((m.latitude  - i.latitude ) / 2)), 2) +
        cos(radians(i.latitude)) * cos(radians(m.latitude)) *
        power(sin(radians((m.longitude - i.longitude) / 2)), 2)
      )) < 120
);

